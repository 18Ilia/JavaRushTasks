package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int column = 0;
        while (column < 5){
            int string = 0;
            while (string < 10){
                System.out.print("Q");
                string++;
            }
            System.out.println();
            column++;
        }

    }
}